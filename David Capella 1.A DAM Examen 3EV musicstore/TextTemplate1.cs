a)  El símbolo - aparece delante de los atributos y el + delante de los métodos. ¿Qué significa cada 
uno en términos de visibilidad? 

El simbolo del guión representa que es privado y el + que es publico, el privado solo puede ser accedido dentro de la clase y el publico puede ser accedido desde cualquier parte del programa.

b)  ¿Qué tipo de dato es disponible y que dos valores puede tener? 

Disponible es de dato booleano y puede tener dos valores, true o false.

c)  Para qué sirve el método ToString() en una clase de C#?

Sirve para convertir los datos de un objeto a una cadena de texto string.